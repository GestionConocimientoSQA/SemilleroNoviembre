package daniela;

public class CiclosyArray {

    public static void main(String[] args) {

        //Inicializando array
        String Vehiculos[] = {"Ford", "Chevrolet", "Mazda"};

        //Utilizando for para imprimir los valores del array
        for (int i= 0; i < Vehiculos.length; i++){
            System.out.println(Vehiculos[i]);
        }

    }
}
